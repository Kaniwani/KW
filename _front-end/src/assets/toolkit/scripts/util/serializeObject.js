// adds to jQuery ability to serialize form into JS object (rather than a url params string)
// usage: let object = $(form).submit(function(ev) { ev.preventDefault(); $(this).serializeObject(); });
$.fn.serializeObject = function() {
  var o = {};
  var a = this.serializeArray();
  $.each(a, function() {
      if (o[this.name] !== undefined) {
          if (!o[this.name].push) {
              o[this.name] = [o[this.name]];
          }
          o[this.name].push(this.value || '');
      } else {
          o[this.name] = this.value || '';
      }
  });
  return o;
};
